find_first_col <- function(df, candidates) {
  hit <- candidates[candidates %in% names(df)]
  if (length(hit) == 0) return(NA_character_)
  hit[1]
}

clean_library_name <- function(x) {
  dplyr::case_when(
    x == "Drug_Perturbations_from_GEO_down" ~ "GEO drug perturbations (down)",
    x == "Drug_Perturbations_from_GEO_up" ~ "GEO drug perturbations (up)",
    x == "DSigDB" ~ "DSigDB",
    x == "LINCS_L1000_Chem_Pert_down" ~ "LINCS L1000 chemical perturbations (down)",
    x == "LINCS_L1000_Chem_Pert_up" ~ "LINCS L1000 chemical perturbations (up)",
    TRUE ~ x
  )
}

clean_term_name <- function(x) {
  x %>%
    str_replace_all("_", " ") %>%
    str_replace_all("&circ;", "^") %>%
    str_replace_all("&sup2;", "2") %>%
    str_replace_all("&alpha;", "alpha") %>%
    str_replace_all("&beta;", "beta") %>%
    str_replace_all("&gamma;", "gamma") %>%
    str_replace_all("&ndash;", "-") %>%
    str_replace_all("&mdash;", "-") %>%
    str_replace_all("\\s+", " ") %>%
    str_trim()
}

standardize_input <- function(df) {
  
  term_col <- find_first_col(df, c("Term", "term", "Description", "Geneset", "Gene_set"))
  
  adj_col <- find_first_col(df, c(
    "Adjusted_P_value", "Adjusted.P.value", "Adjusted P-value",
    "adjusted_p_value", "adj.P.Val", "P.adjust", "padj"
  ))
  
  lib_col <- find_first_col(df, c("Library", "library", "Gene_set_library", "GeneSetLibrary"))
  
  overlap_col <- find_first_col(df, c("Overlapping_genes", "Overlap", "overlap"))
  
  combined_col <- find_first_col(df, c(
    "Combined_score", "Combined.Score", "Combined Score", "combined_score"
  ))
  
  pval_col <- find_first_col(df, c("P_value", "P.value", "P-value", "P_Value", "p_value"))
  
  rank_col <- find_first_col(df, c("Rank", "rank"))
  
  if (is.na(term_col) || is.na(adj_col)) {
    stop("Required columns missing. Need at least Term and adjusted p-value.")
  }
  
  out <- data.frame(
    Rank = if (!is.na(rank_col)) suppressWarnings(as.numeric(df[[rank_col]])) else NA_real_,
    Term = as.character(df[[term_col]]),
    Adjusted_P = suppressWarnings(as.numeric(df[[adj_col]])),
    Library = if (!is.na(lib_col)) as.character(df[[lib_col]]) else "Unknown",
    Overlapping_genes = if (!is.na(overlap_col)) as.character(df[[overlap_col]]) else NA_character_,
    Combined_Score = if (!is.na(combined_col)) suppressWarnings(as.numeric(df[[combined_col]])) else NA_real_,
    P_Value = if (!is.na(pval_col)) suppressWarnings(as.numeric(df[[pval_col]])) else NA_real_,
    stringsAsFactors = FALSE
  )
  
  out %>%
    filter(!is.na(Term), !is.na(Adjusted_P)) %>%
    mutate(
      Library_clean = clean_library_name(Library),
      Term_clean = clean_term_name(Term),
      MinusLog10AdjP = -log10(Adjusted_P)
    )
}

extract_candidate_name <- function(term) {
  
  x <- clean_term_name(term)
  x <- str_trim(x)
  
  x <- str_replace(x, "\\s+DB[0-9]+.*$", "")
  x <- str_replace(x, "\\s+GSE[0-9]+.*$", "")
  x <- str_replace(x, "\\s+GSM[0-9]+.*$", "")
  x <- str_replace(x, "\\s+sample\\s+[0-9]+.*$", "")
  x <- str_replace(x, "\\s+human\\s+GSE[0-9]+.*$", "")
  x <- str_replace(x, "\\s+mouse\\s+GSE[0-9]+.*$", "")
  x <- str_replace(x, "\\s+rat\\s+GSE[0-9]+.*$", "")
  x <- str_replace(x, "\\s+homo sapiens.*$", "")
  x <- str_replace(x, "\\s+mus musculus.*$", "")
  x <- str_replace(x, "\\s+rattus norvegicus.*$", "")
  x <- str_replace(x, "\\s+[0-9]{4,}.*$", "")
  x <- str_replace(x, "\\s+\\d+\\s*(um|µm|nm|mm|hr|h|hours).*$", "")
  x <- str_replace(x, "\\s+\\d+h.*$", "")
  x <- str_replace(x, "\\s+\\d+hr.*$", "")
  x <- str_replace_all(x, "\\s+", " ")
  x <- str_trim(x)
  x <- str_to_title(x)
  
  return(x)
}

get_overlap_genes <- function(x) {
  if (is.na(x) || x == "") return(character(0))
  
  genes <- unlist(str_split(x, ";|,|/|\\s+"))
  genes <- toupper(trimws(genes))
  genes <- genes[genes != ""]
  
  unique(genes)
}

count_overlap_genes <- function(x) {
  length(get_overlap_genes(x))
}

count_gene_hits <- function(overlap_text, gene_set) {
  genes <- get_overlap_genes(overlap_text)
  length(intersect(genes, gene_set))
}

list_gene_hits <- function(overlap_text, gene_set) {
  genes <- get_overlap_genes(overlap_text)
  hits <- intersect(genes, gene_set)
  
  if (length(hits) == 0) return("")
  
  paste(hits, collapse = ";")
}

classify_candidate <- function(candidate_name, term_clean) {
  
  x <- tolower(paste(candidate_name, term_clean))
  
  toxic_or_exposure <- c(
    "cigarette", "smoke", "nickel", "cadmium", "arsenic", "lead",
    "mercury", "hypochlorous", "nitric oxide", "radiation",
    "uv", "ultraviolet", "ozone", "diesel", "silica",
    "lipopolysaccharide", "lps", "endotoxin"
  )
  
  endogenous_or_metabolic <- c(
    "palmitic acid", "glucose", "atp", "cholesterol",
    "oleic acid", "stearic acid", "lactic acid", "pyruvate",
    "hydrogen peroxide", "h2o2"
  )
  
  unclear_signature <- c(
    "brd-", "brdk", "ljp", "trcn", "shrna", "sirna",
    "knockdown", "overexpression"
  )
  
  if (any(str_detect(x, toxic_or_exposure))) {
    return("Exposure_or_toxicant")
  }
  
  if (any(str_detect(x, endogenous_or_metabolic))) {
    return("Endogenous_or_metabolic")
  }
  
  if (any(str_detect(x, unclear_signature))) {
    return("Unmapped_or_genetic_signature")
  }
  
  if (str_detect(x, "interferon|etanercept|adalimumab|infliximab|rituximab|antibody|mab")) {
    return("Biologic_or_immunomodulator")
  }
  
  return("Candidate_drug_or_compound")
}

normalize_01 <- function(x) {
  x <- as.numeric(x)
  x[is.infinite(x)] <- NA
  
  if (all(is.na(x))) return(rep(0, length(x)))
  
  rng <- range(x, na.rm = TRUE)
  
  if (rng[1] == rng[2]) return(rep(1, length(x)))
  
  (x - rng[1]) / (rng[2] - rng[1])
}

save_plot_versions <- function(plot_obj, file_base, width = 12, height = 8) {
  
  ggsave(
    filename = paste0(file_base, ".png"),
    plot = plot_obj,
    width = width,
    height = height,
    dpi = 600,
    bg = "white"
  )
  
  ggsave(
    filename = paste0(file_base, ".pdf"),
    plot = plot_obj,
    width = width,
    height = height,
    bg = "white"
  )
  
  ggsave(
    filename = paste0(file_base, ".tiff"),
    plot = plot_obj,
    width = width,
    height = height,
    dpi = 600,
    compression = "lzw",
    bg = "white"
  )
}

