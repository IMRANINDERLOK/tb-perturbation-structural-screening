candidate_summary <- active_df %>%
  group_by(Candidate_Name, Candidate_Class) %>%
  summarise(
    Supporting_Signatures = n(),
    Libraries = paste(sort(unique(Library_clean)), collapse = "; "),
    Library_Count = n_distinct(Library_clean),
    Best_Adjusted_P = min(Adjusted_P, na.rm = TRUE),
    Max_MinusLog10AdjP = max(MinusLog10AdjP, na.rm = TRUE),
    Mean_MinusLog10AdjP = mean(MinusLog10AdjP, na.rm = TRUE),
    Max_Combined_Score = max(Combined_Score, na.rm = TRUE),
    Mean_Combined_Score = mean(Combined_Score, na.rm = TRUE),
    Max_Overlap_Count = max(Overlap_Count, na.rm = TRUE),
    Total_Overlap_Count = sum(Overlap_Count, na.rm = TRUE),
    Max_Anchor_Hit_Count = max(Anchor_Hit_Count, na.rm = TRUE),
    Max_Core8_Hit_Count = max(Core8_Hit_Count, na.rm = TRUE),
    Anchor_Hits = paste(sort(unique(unlist(str_split(paste(Anchor_Hits, collapse = ";"), ";")))), collapse = ";"),
    Core8_Hits = paste(sort(unique(unlist(str_split(paste(Core8_Hits, collapse = ";"), ";")))), collapse = ";"),
    Top_Term = Term_clean[which.min(Adjusted_P)][1],
    .groups = "drop"
  ) %>%
  mutate(
    Anchor_Hits = str_replace_all(Anchor_Hits, "^;$|^NA$|NA;|;NA", ""),
    Core8_Hits = str_replace_all(Core8_Hits, "^;$|^NA$|NA;|;NA", ""),
    Max_Combined_Score = ifelse(is.infinite(Max_Combined_Score), NA, Max_Combined_Score),
    Mean_Combined_Score = ifelse(is.infinite(Mean_Combined_Score), NA, Mean_Combined_Score),
    Max_Overlap_Count = ifelse(is.infinite(Max_Overlap_Count), 0, Max_Overlap_Count),
    Total_Overlap_Count = ifelse(is.infinite(Total_Overlap_Count), 0, Total_Overlap_Count)
  )

# -----------------------------
# 8. Scoring system
# -----------------------------

candidate_summary <- candidate_summary %>%
  mutate(
    Score_Significance = normalize_01(Max_MinusLog10AdjP),
    Score_Combined = normalize_01(Max_Combined_Score),
    Score_Overlap = normalize_01(Max_Overlap_Count),
    Score_Reproducibility = normalize_01(Supporting_Signatures),
    Score_Library = normalize_01(Library_Count),
    Score_Anchor = ifelse(Max_Anchor_Hit_Count > 0, 1, 0),
    Score_Core8 = normalize_01(Max_Core8_Hit_Count),
    
    Raw_Priority_Score =
      0.25 * Score_Combined +
      0.25 * Score_Significance +
      0.15 * Score_Overlap +
      0.15 * Score_Reproducibility +
      0.10 * Score_Core8 +
      0.05 * Score_Anchor +
      0.05 * Score_Library,
    
    Priority_Score_100 = round(100 * Raw_Priority_Score, 2)
  ) %>%
  arrange(desc(Priority_Score_100), Best_Adjusted_P)

