find_file_recursive <- function(project_dir, file_pattern, required = TRUE) {
  
  matched <- list.files(
    project_dir,
    pattern = file_pattern,
    recursive = TRUE,
    full.names = TRUE
  )
  
  # Avoid picking files generated by this script during reruns
  matched <- matched[!grepl("candidate_curation", matched)]
  
  if (length(matched) == 0 && required) {
    stop(paste("File not found:", file_pattern))
  }
  
  if (length(matched) == 0 && !required) {
    return(NA_character_)
  }
  
  return(matched[1])
}

